//program to display size of datatype


#include <stdio.h>

void main()

{
   int n;
   short a;
   long b;
   float f;
   double d;

   printf("size of int    : %d\n", sizeof(n));
   printf("size of short  : %d\n", sizeof(a));
   printf("size of long   : %d\n", sizeof(b));
   printf("size of float  : %d\n", sizeof(f));
   printf("size of double : %d\n", sizeof(d));


}
